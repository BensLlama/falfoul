export type PixelIconName = keyof typeof ICONS;

/* The six colors of the classic Apple logo, top to bottom. */
const RAINBOW = ["#61bb46", "#fdb827", "#f5821f", "#e03a3e", "#963d97", "#009ddc"];

export function PixelIcon({
  name,
  size = 16,
  className,
  rainbow,
}: {
  name: PixelIconName;
  size?: number;
  className?: string;
  /** Fill with the classic six-color rainbow stripes. */
  rainbow?: boolean;
}) {
  const grid = ICONS[name];

  // Rows that actually contain pixels, for even rainbow banding.
  const inked = grid
    .map((row, y) => (row.includes("X") ? y : -1))
    .filter((y) => y >= 0);
  const first = inked[0] ?? 0;
  const span = Math.max(1, (inked[inked.length - 1] ?? 15) - first + 1);

  return (
    <svg
      viewBox="0 0 16 16"
      width={size}
      height={size}
      className={className}
      aria-hidden
      shapeRendering="crispEdges"
      fill="currentColor"
    >
      {grid.flatMap((row, y) =>
        [...row].map((c, x) =>
          c === "X" ? (
            <rect
              key={`${x}-${y}`}
              x={x}
              y={y}
              width={1}
              height={1}
              fill={
                rainbow
                  ? RAINBOW[
                      Math.min(
                        RAINBOW.length - 1,
                        Math.floor(((y - first) / span) * RAINBOW.length)
                      )
                    ]
                  : undefined
              }
            />
          ) : null
        )
      )}
    </svg>
  );
}
